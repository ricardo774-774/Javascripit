// Funciones que toman parametros y argumentos...

// Para que tus funciones puedan ser
// más versatiles e inteligentes deberán utilizar parametros 
// y argumentos


function saludar(nombre, apellido) { // nombre y apellido son parametros
    console.log( `Hola ${nombre}  ${apellido} ` );
}
saludar('Juan', 'De la torre'); // Pablo y De la torre son argumentos

saludar();
