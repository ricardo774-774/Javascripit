// Veamos como ir incrementando un valor.

// Incluir incremento por 1 y menos 1
let puntaje = 5;
puntaje++;
console.log(puntaje);

puntaje--;
console.log(puntaje);

++puntaje;
console.log(puntaje);

--puntaje;
console.log(puntaje);

// Incluir incremento por 3 y menos 3
puntaje += 3;
console.log(puntaje);

puntaje -= 3;
console.log(puntaje);